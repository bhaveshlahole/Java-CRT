package com.pote.engg.operators;

public class UnaryOperator {

	public static void main(String[] args) {
		int num1 = 10;
		System.out.println(num1 ++); // num1 = num1 + 1
	
		System.out.println(++num1); // num1 = 1 + num1
		
		System.out.println(--num1);
		System.out.println(num1 --);  
		
		System.out.println(num1--);
		System.out.println(num1);
		
	
	}

}
